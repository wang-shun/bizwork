package com.sogou.bizwork.bo.msg.req;

public class AgentUserPhoto {
    private Long agentUserId;
    private String photo;
    private String tempPhoto;
    private String tempPhotoType;
    private String photoType;
    private byte[] data;
    public Long getAgentUserId() {
        return agentUserId;
    }
    public void setAgentUserId(Long agentUserId) {
        this.agentUserId = agentUserId;
    }
    public String getPhoto() {
        return photo;
    }
    public void setPhoto(String photo) {
        this.photo = photo;
    }
    public String getTempPhoto() {
        return tempPhoto;
    }
    public void setTempPhoto(String tempPhoto) {
        this.tempPhoto = tempPhoto;
    }
    public String getTempPhotoType() {
        return tempPhotoType;
    }
    public void setTempPhotoType(String tempPhotoType) {
        this.tempPhotoType = tempPhotoType;
    }
    public String getPhotoType() {
        return photoType;
    }
    public void setPhotoType(String photoType) {
        this.photoType = photoType;
    }
    public byte[] getData() {
        return data;
    }
    public void setData(byte[] data) {
        this.data = data;
    }
}
